public class Marbles {
    public static void main(String[] args) {
        int marbles = 10;
        System.out.println("Number of marbles at the start of the game:");
        System.out.println(marbles);
        marbles--;
        marbles--;
        marbles++;
        System.out.println("Number of marbles at the end of the game:");
        System.out.println(marbles);
    }
}
